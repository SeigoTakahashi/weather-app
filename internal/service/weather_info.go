// GetPracticalAdvice は数値データから生活に役立つアドバイスを生成します
func GetPracticalAdvice(w *weather.WeatherResponse) []string {
    var advices []string

    // 気温による服装・体調アドバイス
    temp := w.Main.Temp
    switch {
    case temp >= 35:
        advices = append(advices, "猛暑です。外出は控え、冷房を適切に使用してください。")
    case temp >= 30:
        advices = append(advices, "真夏日です。こまめな水分補給を忘れずに。")
    case temp >= 25:
        advices = append(advices, "暑くなります。半袖で過ごせますが、紫外線対策を。")
    case temp >= 15:
        advices = append(advices, "過ごしやすい気温です。長袖のシャツがちょうど良いでしょう。")
    case temp >= 5:
        advices = append(advices, "肌寒いです。コートや厚手の上着を用意しましょう。")
    default:
        advices = append(advices, "厳しい寒さです。防寒対策を万全に。路面凍結にも注意。")
    }

    // 湿度による快適度アドバイス
    humidity := w.Main.Humidity
    if temp >= 20 { // 暖かい時期の湿度チェック
        if humidity >= 70 {
            advices = append(advices, "蒸し暑さを感じそうです。除湿などで調整しましょう。")
        } else if humidity <= 30 {
            advices = append(advices, "空気が乾燥しています。お肌のケアを。")
        }
    } else { // 寒い時期の湿度チェック
        if humidity <= 40 {
            advices = append(advices, "乾燥して風邪を引きやすい環境です。加湿器を使いましょう。")
        }
    }

    // 風速による影響アドバイス
    speed := w.Wind.Speed
    switch {
    case speed >= 15:
        advices = append(advices, "非常に強い風です。飛来物や公共交通機関の乱れに注意。")
    case speed >= 10:
        advices = append(advices, "強い風が吹いています。傘が煽られやすいので気をつけて。")
    case speed >= 5:
        advices = append(advices, "やや風があります。体感温度は表示より低く感じられそうです。")
    }

    return advices
}

